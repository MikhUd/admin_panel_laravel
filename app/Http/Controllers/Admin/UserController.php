     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $users = User::paginate(10);
        
        $user_roles = DB::select('select  u.id , r.name  from admin_laravel.roles as r inner join admin_laravel.model_has_roles 
        as m on m.role_id = r.id inner join admin_laravel.users as u on u.id = m.model_id order by u.id');

        $user_ids = [];

        for($i = 0; $i < count($users); $i++){
            $user_ids[$users[$i]['id']] = $i;
        }

        foreach($user_roles as $role){
            $users[$user_ids[$role->id]]->role = $role->name;
        }
        
        return view('admin.user.index',[
            'users' => $users,
        ]);
    }

